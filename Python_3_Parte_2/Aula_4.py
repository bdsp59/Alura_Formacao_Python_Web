#Tupla na verdade é uma Lista bloqueada, ou seja, não pode mudar o que foi atribuido a ela.
#A diferença para declarar uma tupla e uma lista é que a lista é declarado com [] e uma tupla com ()

dias = ["S", "T", "Q"] #Exemplo de lista
dias = ("Q", "S", "S", "D") #Exemplo de tupla

#Também temos uma estrutura de dados, que não permite elementos duplicados, o set.
#Para iniciar o set usamos {} e para adicionar um elemento ao set utilizamos o .add(), mas não adiciona se o elemento já existir.
#O set não pode ser chamado de sequência, pois não tem um índice e o set não é ordenado.

